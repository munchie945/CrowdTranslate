public class User {

    private String key;
    private String username;

    public User(){}

    public String getKey() {
        return key;
    }

    public String getUsername() {
        return username;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public void setUsername(String username) {
        this.username = username;
    }

}
